    private void mnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnDeleteActionPerformed
        int result = JOptionPane.showConfirmDialog(rootPane,
                "Apakah Yakin untuk Didelete?",
                "Delete Dialog", JOptionPane.YES_NO_OPTION);
        if(result == JOptionPane.YES_OPTION){
            String nik = tblLogin.getModel().getValueAt(tblLogin.getSelectedRow(), 0).toString();
            controllerLogin.deleteLogin(nik);
            setClear();
        }
    }//GEN-LAST:event_mnDeleteActionPerformed

    private void setTableLogin(List<Login> listLogin) {
        if(listLogin==null){
          JOptionPane.showMessageDialog(this, "Data login gagal diambil dari database",
                    "DATABASE FAILED",JOptionPane.ERROR_MESSAGE);
        }else{
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(
                new String[] {
                    "NIK",
                    "Username",
                    "Password"
                }
            );
            
            for (Login l : listLogin) {
                Object[] o = new Object[5];
                o[0] = l.getNik();
                o[1] = l.getUsername();
                o[2] = l.getPassword();
                tableModel.addRow(o);
            }
            tblLogin.setAutoResizeMode( javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS );
            tblLogin.setModel(tableModel);
        }
    }
    
    private void setComboLogin(List<Karyawan> listKaryawan){
        if (listKaryawan==null){
            JOptionPane.showMessageDialog(this, "Data login gagal diambil dari database",
                    "DATABASE FAILED",JOptionPane.ERROR_MESSAGE);
        }else{
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            for(Karyawan k : listKaryawan){
                model.addElement(k.getNik());
            }
            cbxNik.setModel(model);
        }
    }
    
    private void setClear(){
        cbxNik.setSelectedIndex(0);
        txtUsername.setText("");
        pwPassword.setText("");
        btnSimpan.setText("Simpan");
        setTableLogin(controllerLogin.getAllData());
    }
    
