    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nik = cbxNik.getSelectedItem().toString();
        String username = txtUsername.getText();
        String password = pwPassword.getText();
        
        Login data = new Login(
                nik,
                username,
                password
        );
        
        if(btnSimpan.getText().equals("Simpan")){
            if(controllerLogin.insertLogin(data)){
                outCommand.setText("Insert Berhasil");
            }else{
                outCommand.setText("Insert Gagal");
            }    
        } else{
            if(controllerLogin.updateLogin(data)){
                outCommand.setText("Update Berhasil");
            }else{
                outCommand.setText("Update Gagal");
            }  
        }
        setClear();
    }//GEN-LAST:event_btnSimpanActionPerformed

