    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String tnkb_pelanggan = txtTnkbPelanggan.getText();
        String nama_pelanggan = txtNamaPelanggan.getText();
        String alamat_pelanggan = txtAlamatPelanggan.getText();
        
        Pelanggan data = new Pelanggan(
                tnkb_pelanggan,
                nama_pelanggan,
                alamat_pelanggan
        );
        
        if(btnSimpan.getText().equals("Simpan")){
            if(controllerPelanggan.insertPelanggan(data)){
                outCommand.setText("Insert Berhasil");
            }
            else{
                outCommand.setText("Insert Gagal");
            }
        }
        else{
            if(controllerPelanggan.updatePelanggan(data)){
                outCommand.setText("Update Berhasil");
            }
            else{
                outCommand.setText("Update Gagal");
            }
        }
        setClear();
    }//GEN-LAST:event_btnSimpanActionPerformed

