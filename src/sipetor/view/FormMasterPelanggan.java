    private void setTablePelanggan(List<Pelanggan> listPelanggan){
        if(listPelanggan==null){
            JOptionPane.showMessageDialog(this, "Data " 
                  + "Golongan gagal diambil dari database","DATABASE FAILED",JOptionPane.ERROR_MESSAGE);
        }
        else{
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(
                new String[] {
                    "TNKB Pelanggan",
                    "Nama Pelanggan",
                    "Alamat Pelanggan"
                }
            );
            
            for (Pelanggan p : listPelanggan) {
                Object [] o = new Object[3];
                o[0] = p.getTnkb_pelanggan();
                o[1] = p.getNama_pelanggan();
                o[2] = p.getAlamat_pelanggan();
                tableModel.addRow(o);
            }
            tblPelanggan.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblPelanggan.setModel(tableModel);
        }
    }
    
