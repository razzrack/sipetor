    private void tblPelangganMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPelangganMousePressed
        if(evt.getClickCount()==2){
            int idxrow = tblPelanggan.getSelectedRow();
            txtTnkbPelanggan.setText(tblPelanggan.getModel().getValueAt(idxrow, 0).toString());
            txtNamaPelanggan.setText(tblPelanggan.getModel().getValueAt(idxrow, 1).toString());
            txtAlamatPelanggan.setText(tblPelanggan.getModel().getValueAt(idxrow, 2).toString());
            btnSimpan.setText("Update");
        }
        if(evt.isPopupTrigger()){
            popupPelanggan.show(tblPelanggan, evt.getX(), evt.getY());
        }
    }//GEN-LAST:event_tblPelangganMousePressed

    private void tblPelangganMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPelangganMouseReleased
        if(evt.getClickCount()==2){
            int idxrow = tblPelanggan.getSelectedRow();
            txtTnkbPelanggan.setText(tblPelanggan.getModel().getValueAt(idxrow, 0).toString());
            txtNamaPelanggan.setText(tblPelanggan.getModel().getValueAt(idxrow, 1).toString());
            txtAlamatPelanggan.setText(tblPelanggan.getModel().getValueAt(idxrow, 2).toString());
            btnSimpan.setText("Update");
        }
        if(evt.isPopupTrigger()){
            popupPelanggan.show(tblPelanggan, evt.getX(), evt.getY());
        }
    }//GEN-LAST:event_tblPelangganMouseReleased

    private void mnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnDeleteActionPerformed
        int result = JOptionPane.showConfirmDialog(rootPane,
                "Apakah Yakin untuk Didelete?",
                "Delete Dialog", JOptionPane.YES_NO_OPTION);
        if(result == JOptionPane.YES_OPTION){
            String tnkb = tblPelanggan.getModel().getValueAt(tblPelanggan.getSelectedRow(), 0).toString();
            controllerPelanggan.deletePelanggan(tnkb);
            setClear();
        }
    }//GEN-LAST:event_mnDeleteActionPerformed

